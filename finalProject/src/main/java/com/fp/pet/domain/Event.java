package com.fp.pet.domain;

public class Event {
	private long specialNum;
	private long productNum;
	private long special;
	private String subject;
	private String content;
	private String imageFileName;
	private String startDate;
	private String endDate;
	private String reg_date;
	private long hitCount;
	private long showSpecial;
	
	public long getSpecialNum() {
		return specialNum;
	}
	public void setSpecialNum(long specialNum) {
		this.specialNum = specialNum;
	}
	public long getProductNum() {
		return productNum;
	}
	public void setProductNum(long productNum) {
		this.productNum = productNum;
	}
	public long getSpecial() {
		return special;
	}
	public void setSpecial(long special) {
		this.special = special;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getImageFileName() {
		return imageFileName;
	}
	public void setImageFileName(String imageFileName) {
		this.imageFileName = imageFileName;
	}
	public String getStartDate() {
		return startDate;
	}
	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}
	public String getEndDate() {
		return endDate;
	}
	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}
	public String getReg_date() {
		return reg_date;
	}
	public void setReg_date(String reg_date) {
		this.reg_date = reg_date;
	}
	public long getHitCount() {
		return hitCount;
	}
	public void setHitCount(long hitCount) {
		this.hitCount = hitCount;
	}
	public long getShowSpecial() {
		return showSpecial;
	}
	public void setShowSpecial(long showSpecial) {
		this.showSpecial = showSpecial;
	}
	
	
}
